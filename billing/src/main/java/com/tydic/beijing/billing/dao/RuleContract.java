package com.tydic.beijing.billing.dao;

import java.io.Serializable;
import java.sql.Date;

public class RuleContract implements Serializable {
	private static final long serialVersionUID = 1L;

	private String contract_id;
	private String contract_name;
	private Date create_time;
	private String eff_flag;
	private String remark;
	public String getContract_id() {
		return contract_id;
	}
	public void setContract_id(String contract_id) {
		this.contract_id = contract_id;
	}
	public String getContract_name() {
		return contract_name;
	}
	public void setContract_name(String contract_name) {
		this.contract_name = contract_name;
	}
	public Date getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}
	public String getEff_flag() {
		return eff_flag;
	}
	public void setEff_flag(String eff_flag) {
		this.eff_flag = eff_flag;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	@Override
	public String toString() {
		return "RuleContract [contract_id=" + contract_id + ", contract_name="
				+ contract_name + ", create_time=" + create_time
				+ ", eff_flag=" + eff_flag + ", remark=" + remark + "]";
	}
}
