package com.tydic.beijing.billing.dto;

import java.io.Serializable;

public class ResourcePara implements Serializable {

	/**
	 * YUANDAO
	 */
	private static final long serialVersionUID = 1L;
	private String staff_id;// 受理员工
	private String channel_id;// 接触渠道
	private String external_system_id;// 接入系统
	private String path;
	private int channel_no;//通道号
	private String msisnd_send;

	public int getChannel_no() {
		return channel_no;
	}

	public void setChannel_no(int channel_no) {
		this.channel_no = channel_no;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getStaff_id() {
		return staff_id;
	}

	public void setStaff_id(String staff_id) {
		this.staff_id = staff_id;
	}

	public String getChannel_id() {
		return channel_id;
	}

	public void setChannel_id(String channel_id) {
		this.channel_id = channel_id;
	}

	public String getExternal_system_id() {
		return external_system_id;
	}

	public void setExternal_system_id(String external_system_id) {
		this.external_system_id = external_system_id;
	}

	public String getMsisnd_send() {
		return msisnd_send;
	}

	public void setMsisnd_send(String msisnd_send) {
		this.msisnd_send = msisnd_send;
	}

	public String toString() {
		return "ResourcePara [staff_id=" + staff_id + ",channel_id="
				+ channel_id + ",external_system_id=" + external_system_id
				+",path="+path+",channel_no="+channel_no+",msisnd_send="+msisnd_send+"]";
	}

}
