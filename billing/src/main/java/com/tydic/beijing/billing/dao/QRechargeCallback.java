package com.tydic.beijing.billing.dao;

import java.io.Serializable;

/**
 * 充值触发充值回调
 * 
 * @author Tian
 *
 */
public class QRechargeCallback implements Serializable {
	private static final long serialVersionUID = 1L;

	private String serial_no;
	private String device_number;
	private String user_id;
	private String pay_id;
	private String recharge_detail;
	private String callbackurl;
	private String pay_time;
	private int state;
	private String process_failed_desc;

	public String getProcess_failed_desc() {
		return process_failed_desc;
	}

	public void setProcess_failed_desc(String process_failed_desc) {
		this.process_failed_desc = process_failed_desc;
	}

	public String getSerial_no() {
		return serial_no;
	}

	public void setSerial_no(String serial_no) {
		this.serial_no = serial_no;
	}

	public String getDevice_number() {
		return device_number;
	}

	public void setDevice_number(String device_number) {
		this.device_number = device_number;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getPay_id() {
		return pay_id;
	}

	public void setPay_id(String pay_id) {
		this.pay_id = pay_id;
	}

	public String getRecharge_detail() {
		return recharge_detail;
	}

	public void setRecharge_detail(String recharge_detail) {
		this.recharge_detail = recharge_detail;
	}

	public String getCallbackurl() {
		return callbackurl;
	}

	public void setCallbackurl(String callbackurl) {
		this.callbackurl = callbackurl;
	}

	public String getPay_time() {
		return pay_time;
	}

	public void setPay_time(String pay_time) {
		this.pay_time = pay_time;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	@Override
	public String toString() {
		return "QRechargeCallback [serial_no=" + serial_no + ", device_number=" + device_number + ", user_id=" + user_id
				+ ", pay_id=" + pay_id + ", recharge_detail=" + recharge_detail + ", callbackurl=" + callbackurl
				+ ", pay_time=" + pay_time + ", state=" + state + ", process_failed_desc=" + process_failed_desc + "]";
	}
	
}
