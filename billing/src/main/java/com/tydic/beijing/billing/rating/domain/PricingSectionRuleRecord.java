package com.tydic.beijing.billing.rating.domain;

public class PricingSectionRuleRecord {

	private long pricingSectionId=0;
    private long  upPricingSectionId=0;
    private long  priority=0;
    private int  condId=0;
    private long lower=0;
    private long  upper=0;
    private int  refFlag=0;
    private String refResourceCode="";
    
    
    public PricingSectionRuleRecord(long sectionId,long upSectionId,long priority,int condId,long lower,
    		long upper,int refFlag,String resorceCode){
    	this.pricingSectionId=sectionId;
    	this.upPricingSectionId=upSectionId;
    	this.priority=priority;
    	this.condId=condId;
    	this.lower=lower;
    	this.upper=upper;
    	this.refFlag=refFlag;
    	this.refResourceCode=resorceCode;
    }
    public PricingSectionRuleRecord(){}
    
	public long getPricingSectionId() {
		return pricingSectionId;
	}
	public void setPricingSectionId(long pricingSectionId) {
		this.pricingSectionId = pricingSectionId;
	}
	public long getUpPricingSectionId() {
		return upPricingSectionId;
	}
	public void setUpPricingSectionId(long upPricingSectionId) {
		this.upPricingSectionId = upPricingSectionId;
	}
	public long getPriority() {
		return priority;
	}
	public void setPriority(long priority) {
		this.priority = priority;
	}
	public int getCondId() {
		return condId;
	}
	public void setCondId(int condId) {
		this.condId = condId;
	}
	public long getLower() {
		return lower;
	}
	public void setLower(long lower) {
		this.lower = lower;
	}
	public long getUpper() {
		return upper;
	}
	public void setUpper(long upper) {
		this.upper = upper;
	}
	public int getRefFlag() {
		return refFlag;
	}
	public void setRefFlag(int refFlag) {
		this.refFlag = refFlag;
	}
	public String getRefResourceCode() {
		return refResourceCode;
	}
	public void setRefResourceCode(String refResourceCode) {
		this.refResourceCode = refResourceCode;
	}
    
    
    
    
}
