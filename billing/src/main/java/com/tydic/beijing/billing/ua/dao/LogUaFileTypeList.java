package com.tydic.beijing.billing.ua.dao;

import java.io.Serializable;

public class LogUaFileTypeList implements Serializable {

	private static final long serialVersionUID = 1L;
	public String file_type;
	public String raw_file_name;
	public String file_name;
	public String dst_dir;
	public int record_count;
	public long break_point;

	public final String getFile_type() {
		return file_type;
	}

	public final void setFile_type(String file_type) {
		this.file_type = file_type;
	}

	public final String getRaw_file_name() {
		return raw_file_name;
	}

	public final void setRaw_file_name(String raw_file_name) {
		this.raw_file_name = raw_file_name;
	}

	public final String getFile_name() {
		return file_name;
	}

	public final void setFile_name(String file_name) {
		this.file_name = file_name;
	}

	public final String getDst_dir() {
		return dst_dir;
	}

	public final void setDst_dir(String dst_dir) {
		this.dst_dir = dst_dir;
	}

	public final int getRecord_count() {
		return record_count;
	}

	public final void setRecord_count(int record_count) {
		this.record_count = record_count;
	}

	public final long getBreak_point() {
		return break_point;
	}

	public final void setBreak_point(long break_point) {
		this.break_point = break_point;
	}

	@Override
	public String toString() {
		return "LogUaFileTypeList [file_type=" + file_type + ", raw_file_name=" + raw_file_name
				+ ", file_name=" + file_name + ", dst_dir=" + dst_dir + ", record_count="
				+ record_count + ", break_point=" + break_point + "]";
	}

	public void clear() {
		file_type = "";
		raw_file_name = "";
		file_name = "";
		dst_dir = "";
		record_count = 0;
		break_point = 0;
	}
}
