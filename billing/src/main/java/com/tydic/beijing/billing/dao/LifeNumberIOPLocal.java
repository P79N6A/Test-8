package com.tydic.beijing.billing.dao;

public class LifeNumberIOPLocal {
	private String user_id;
	private String device_number;
	private String start_time;
	private String end_time;
	
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getDevice_number() {
		return device_number;
	}
	public void setDevice_number(String device_number) {
		this.device_number = device_number;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	
	public LifeNumberIOPLocal(String device_number, String user_id, String start_time,
			String end_time) {
		super();
		this.device_number = device_number;
		this.user_id = user_id;
		this.start_time = start_time;
		this.end_time = end_time;
	}
	
	public LifeNumberIOPLocal(LifeNumberIOP lni) {
	
		this.device_number = lni.getDevice_number();
		this.user_id = lni.getUser_id();
		this.start_time = lni.getStart_time();
		this.end_time = lni.getEnd_time();
	}

}
