package com.tydic.beijing.billing.dto;

import java.io.Serializable;
import java.util.List;

public class Resource2AccountInfo  implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private String UserId;
	
	private String ContactChannel;
	
	private List<Res2AcctProductInfo> lProductInfo;
	

	public String getUserId() {
		return UserId;
	}

	public void setUserId(String userId) {
		UserId = userId;
	}

	public String getContactChannel() {
		return ContactChannel;
	}

	public void setContactChannel(String contactChannel) {
		ContactChannel = contactChannel;
	}

	public List<Res2AcctProductInfo> getlProductInfo() {
		return lProductInfo;
	}

	public void setlProductInfo(List<Res2AcctProductInfo> lProductInfo) {
		this.lProductInfo = lProductInfo;
	}

	public String toString(){
		return "Resource2AccountInfo [UserId=" + UserId + ",ContactChannel=" + ContactChannel + lProductInfo.toString();
	}
	
}
