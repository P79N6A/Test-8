package com.tydic.beijing.billing.dto;

import java.io.Serializable;

public class CDRDto implements Serializable{
	private static final long serialVersionUID = 1L;
	private String ServiceType;
	private String StartTime;
	private String RomaType;
	private String CallType;
	private long Duration;
	private long DataTrafic;
	private String CalledNumber;
	private long Charge;
	
	private String LongDistanceType;
	private String VisitedCity;
	private String callId;
	private String recordUrl;
	
	public String getServiceType() {
		return ServiceType;
	}
	public void setServiceType(String serviceType) {
		ServiceType = serviceType;
	}
	public String getStartTime() {
		return StartTime;
	}
	public void setStartTime(String startTime) {
		StartTime = startTime;
	}
	public String getRomaType() {
		return RomaType;
	}
	public void setRomaType(String romaType) {
		RomaType = romaType;
	}
	public String getCallType() {
		return CallType;
	}
	public void setCallType(String callType) {
		CallType = callType;
	}
	public long getDuration() {
		return Duration;
	}
	public void setDuration(long duration) {
		Duration = duration;
	}
	public long getDataTrafic() {
		return DataTrafic;
	}
	public void setDataTrafic(long dataTrafic) {
		DataTrafic = dataTrafic;
	}
	public String getCalledNumber() {
		return CalledNumber;
	}
	public void setCalledNumber(String calledNumber) {
		CalledNumber = calledNumber;
	}
	public long getCharge() {
		return Charge;
	}
	public void setCharge(long charge) {
		Charge = charge;
	}
	public String getLongDistanceType() {
		return LongDistanceType;
	}
	public void setLongDistanceType(String longDistanceType) {
		LongDistanceType = longDistanceType;
	}
	public String getVisitedCity() {
		return VisitedCity;
	}
	public void setVisitedCity(String visitedCity) {
		VisitedCity = visitedCity;
	}

	public String getCallId() {
		return callId;
	}

	public void setCallId(String callId) {
		this.callId = callId;
	}

	public String getRecordUrl() {
		return recordUrl;
	}

	public void setRecordUrl(String recordUrl) {
		this.recordUrl = recordUrl;
	}

	@Override
	public String toString() {
		return "CDRDto{" +
				"ServiceType='" + ServiceType + '\'' +
				", StartTime='" + StartTime + '\'' +
				", RomaType='" + RomaType + '\'' +
				", CallType='" + CallType + '\'' +
				", Duration=" + Duration +
				", DataTrafic=" + DataTrafic +
				", CalledNumber='" + CalledNumber + '\'' +
				", Charge=" + Charge +
				", LongDistanceType='" + LongDistanceType + '\'' +
				", VisitedCity='" + VisitedCity + '\'' +
				", callId='" + callId + '\'' +
				", recordUrl='" + recordUrl + '\'' +
				'}';
	}
}
