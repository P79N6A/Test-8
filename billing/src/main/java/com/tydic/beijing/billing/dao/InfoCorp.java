package com.tydic.beijing.billing.dao;

import java.io.Serializable;

public class InfoCorp implements Serializable {
	private static final long serialVersionUID = 1L;
	private String corp_id;
	private String corp_name;
	private String corp_type;
	private String corp_user_id;
	private String admin_user_id;
	private String admin_dev_num;
	private String eff_flag;
	private String prior_corp_id;
	private String corp_level;
	public String getCorp_id() {
		return corp_id;
	}
	public void setCorp_id(String corp_id) {
		this.corp_id = corp_id;
	}
	public String getCorp_name() {
		return corp_name;
	}
	public void setCorp_name(String corp_name) {
		this.corp_name = corp_name;
	}
	public String getCorp_type() {
		return corp_type;
	}
	public void setCorp_type(String corp_type) {
		this.corp_type = corp_type;
	}
	public String getCorp_user_id() {
		return corp_user_id;
	}
	public void setCorp_user_id(String corp_user_id) {
		this.corp_user_id = corp_user_id;
	}
	public String getAdmin_user_id() {
		return admin_user_id;
	}
	public void setAdmin_user_id(String admin_user_id) {
		this.admin_user_id = admin_user_id;
	}
	public String getAdmin_dev_num() {
		return admin_dev_num;
	}
	public void setAdmin_dev_num(String admin_dev_num) {
		this.admin_dev_num = admin_dev_num;
	}
	public String getEff_flag() {
		return eff_flag;
	}
	public void setEff_flag(String eff_flag) {
		this.eff_flag = eff_flag;
	}
	public String getPrior_corp_id() {
		return prior_corp_id;
	}
	public void setPrior_corp_id(String prior_corp_id) {
		this.prior_corp_id = prior_corp_id;
	}
	public String getCorp_level() {
		return corp_level;
	}
	public void setCorp_level(String corp_level) {
		this.corp_level = corp_level;
	}
	
	@Override
	public String toString() {
		return "InfoCorp [corp_id=" + corp_id + ", corp_name=" + corp_name + ", corp_type=" + corp_type
				+ ", corp_user_id=" + corp_user_id + ", admin_user_id=" + admin_user_id + ", admin_dev_num="
				+ admin_dev_num + ", eff_flag=" + eff_flag + ", prior_corp_id=" + prior_corp_id + ", corp_level="
				+ corp_level + "]";
	}
	
	
	
}
