package com.tydic.beijing.billing.dto;

import java.io.Serializable;
import java.util.List;

public class BValueExchangeRequest implements Serializable {
	private static final long serialVersionUID = 1L;

	private String Sn;
	private String MSISDN;
	private String ContactChannel;
	private List<PackageDetailDto> PackageDetailDtoList;
	private String JDPin;

	public String getSn() {
		return Sn;
	}

	public void setSn(String sn) {
		Sn = sn;
	}

	public String getMSISDN() {
		return MSISDN;
	}

	public void setMSISDN(String mSISDN) {
		MSISDN = mSISDN;
	}

	public String getContactChannel() {
		return ContactChannel;
	}

	public void setContactChannel(String contactChannel) {
		ContactChannel = contactChannel;
	}

	public List<PackageDetailDto> getPackageDetailDtoList() {
		return PackageDetailDtoList;
	}

	public void setPackageDetailDtoList(
			List<PackageDetailDto> packageDetailDtoList) {
		PackageDetailDtoList = packageDetailDtoList;
	}

	public String getJDPin() {
		return JDPin;
	}

	public void setJDPin(String jDPin) {
		JDPin = jDPin;
	}

	@Override
	public String toString() {
		return "BValueExchangeRequest [Sn=" + Sn + ", MSISDN=" + MSISDN
				+ ", ContactChannel=" + ContactChannel
				+ ", PackageDetailDtoList=" + PackageDetailDtoList + ", JDPin="
				+ JDPin + "]";
	}
}
