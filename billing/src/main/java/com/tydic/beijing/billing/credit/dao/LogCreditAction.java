package com.tydic.beijing.billing.credit.dao;

import java.io.Serializable;

public class LogCreditAction implements Serializable {

	private static final long serialVersionUID = 1L;
	private long serial_no;
	private String user_id;
	private String pay_id;
	private long credit_number;
	private long history;
	private long realtime;
	private String reason;
	private String action_time;
	private String source;// 0 q_block, 1 file

	public long getSerial_no() {
		return serial_no;
	}

	public void setSerial_no(long serial_no) {
		this.serial_no = serial_no;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getPay_id() {
		return pay_id;
	}

	public void setPay_id(String pay_id) {
		this.pay_id = pay_id;
	}

	public long getCredit_number() {
		return credit_number;
	}

	public void setCredit_number(long credit_number) {
		this.credit_number = credit_number;
	}

	public long getHistory() {
		return history;
	}

	public void setHistory(long history) {
		this.history = history;
	}

	public long getRealtime() {
		return realtime;
	}

	public void setRealtime(long realtime) {
		this.realtime = realtime;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getAction_time() {
		return action_time;
	}

	public void setAction_time(String action_time) {
		this.action_time = action_time;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	@Override
	public String toString() {
		return "LogCreditAction [serial_no=" + serial_no + ", user_id=" + user_id + ", pay_id="
				+ pay_id + ", credit_number=" + credit_number + ", history=" + history
				+ ", realtime=" + realtime + ", reason=" + reason + ", action_time=" + action_time
				+ ", source=" + source + "]";
	}

}
