package com.tydic.beijing.billing.rating.domain;

import java.util.Date;

public class InfoUserReason {
	private String service_sn;
	private String user_id;
	private String reason_id;
	private String local_net;
	private Date eff_date;
	private String cause_desc;
	private String status_code;
	private long prior_level;
	public String getService_sn() {
		return service_sn;
	}
	public void setService_sn(String service_sn) {
		this.service_sn = service_sn;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getReason_id() {
		return reason_id;
	}
	public void setReason_id(String reason_id) {
		this.reason_id = reason_id;
	}
	public String getLocal_net() {
		return local_net;
	}
	public void setLocal_net(String local_net) {
		this.local_net = local_net;
	}
	public Date getEff_date() {
		return eff_date;
	}
	public void setEff_date(Date eff_date) {
		this.eff_date = eff_date;
	}
	public String getCause_desc() {
		return cause_desc;
	}
	public void setCause_desc(String cause_desc) {
		this.cause_desc = cause_desc;
	}
	public String getStatus_code() {
		return status_code;
	}
	public void setStatus_code(String status_code) {
		this.status_code = status_code;
	}
	public long getPrior_level() {
		return prior_level;
	}
	public void setPrior_level(long prior_level) {
		this.prior_level = prior_level;
	}
	@Override
	public String toString() {
		return "InfoUserReason [service_sn=" + service_sn + ", user_id=" + user_id + ", reason_id=" + reason_id
				+ ", local_net=" + local_net + ", eff_date=" + eff_date + ", cause_desc=" + cause_desc
				+ ", status_code=" + status_code + ", prior_level=" + prior_level + "]";
	}
	
	
}
