package com.tydic.beijing.billing.dto;

import java.io.Serializable;
import javax.persistence.Id;

public final class ResourceChargeParaOutList implements Serializable {
	private static final long serialVersionUID = 1L;
	
	private long BalanceTypeId;
	private String EffDate;
	private String ExpDate;
	private long OldBalance;
	private long NewBalance;
	private String BalanceId;
	
	@Id
	public long getBalanceTypeId() {
		return BalanceTypeId;
	}
	public void setBalanceTypeId(long balanceTypeId) {
		BalanceTypeId = balanceTypeId;
	}
	public String getEffDate() {
		return EffDate;
	}
	public void setEffDate(String effDate) {
		EffDate = effDate;
	}
	public String getExpDate() {
		return ExpDate;
	}
	public void setExpDate(String expDate) {
		ExpDate = expDate;
	}
	public long getNewBalance() {
		return NewBalance;
	}
	public void setNewBalance(long newBalance) {
		NewBalance = newBalance;
	}
	public long getOldBalance() {
		return OldBalance;
	}
	public void setOldBalance(long oldBalance) {
		OldBalance = oldBalance;
	}
	public String getBalanceId() {
		return BalanceId;
	}
	public void setBalanceId(String balanceId) {
		BalanceId = balanceId;
	}
	@Override
	public String toString() {
		return "ResourceChargeParaOutList [BalanceTypeId=" + BalanceTypeId
				+ ", EffDate=" + EffDate + ", ExpDate=" + ExpDate
				+ ", OldBalance=" + OldBalance + ", NewBalance=" + NewBalance
				+ ", BalanceId=" + BalanceId + "]";
	}
	

}
