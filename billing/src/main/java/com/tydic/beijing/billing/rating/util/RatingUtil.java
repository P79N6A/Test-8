package com.tydic.beijing.billing.rating.util;

public class RatingUtil {
	
	public static String[] getSplitString(String callList, String splitTag)
	{
		if (callList != null && !callList.equals("") && splitTag != null && !splitTag.equals(""))
		{
			String[] str = callList.split(splitTag);
			return str;
		} else
		{
			return null;
		}
	}
	
	
	static boolean isLeapYear(int year)
    {
        return (year % 4) == 0 && ((year % 100) != 0 || (year % 400) == 0);
    }
	
	
	
	
	

}
