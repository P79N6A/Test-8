package com.tydic.beijing.billing.rating.domain;

import java.util.Date;

public class RatingDeductLog {

	
	private String userid;
	private String reqmsg ;
	private String balanceid ;
	private String deductvalue ;
	private String newbalance ;
	private Date deducttime;
	
	private String partitionId ;//用于分表
 
	public String getPartitionId() {
		return partitionId;
	}
	public void setPartitionId(String partitionId) {
		this.partitionId = partitionId;
	}
	public String getReqmsg() {
		return reqmsg;
	}
	public void setReqmsg(String reqmsg) {
		this.reqmsg = reqmsg;
	}
	public String getBalanceid() {
		return balanceid;
	}
	public void setBalanceid(String balanceid) {
		this.balanceid = balanceid;
	}
	public String getDeductvalue() {
		return deductvalue;
	}
	public void setDeductvalue(String deductvalue) {
		this.deductvalue = deductvalue;
	}
	public String getNewbalance() {
		return newbalance;
	}
	public void setNewbalance(String newbalance) {
		this.newbalance = newbalance;
	}
	public Date getDeducttime() {
		return deducttime;
	}
	public void setDeducttime(Date deducttime) {
		this.deducttime = deducttime;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	

	
	
	
}
