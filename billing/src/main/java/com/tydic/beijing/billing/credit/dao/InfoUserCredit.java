package com.tydic.beijing.billing.credit.dao;

import java.io.Serializable;

public class InfoUserCredit implements Serializable {
	private static final long serialVersionUID = 1L;
	public String user_credit_id;
	public String user_id;
	public String eff_date;
	public String exp_date;
	public String eff_flag;
	public String local_net;
	public String credit_type;
	public long credit_number;
	public String jd_acct_id;
	public String act_status;

	public String getUser_credit_id() {
		return user_credit_id;
	}

	public void setUser_credit_id(String user_credit_id) {
		this.user_credit_id = user_credit_id;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getEff_date() {
		return eff_date;
	}

	public void setEff_date(String eff_date) {
		this.eff_date = eff_date;
	}

	public String getExp_date() {
		return exp_date;
	}

	public void setExp_date(String exp_date) {
		this.exp_date = exp_date;
	}

	public String getEff_flag() {
		return eff_flag;
	}

	public void setEff_flag(String eff_flag) {
		this.eff_flag = eff_flag;
	}

	public String getLocal_net() {
		return local_net;
	}

	public void setLocal_net(String local_net) {
		this.local_net = local_net;
	}

	public String getCredit_type() {
		return credit_type;
	}

	public void setCredit_type(String credit_type) {
		this.credit_type = credit_type;
	}

	public long getCredit_number() {
		return credit_number;
	}

	public void setCredit_number(long credit_number) {
		this.credit_number = credit_number;
	}

	public String getJd_acct_id() {
		return jd_acct_id;
	}

	public void setJd_acct_id(String jd_acct_id) {
		this.jd_acct_id = jd_acct_id;
	}

	public String getAct_status() {
		return act_status;
	}

	public void setAct_status(String act_status) {
		this.act_status = act_status;
	}

	@Override
	public String toString() {
		return "InfoUserCredit [user_credit_id=" + user_credit_id + ", user_id=" + user_id
				+ ", eff_date=" + eff_date + ", exp_date=" + exp_date + ", eff_flag=" + eff_flag
				+ ", local_net=" + local_net + ", credit_type=" + credit_type + ", credit_number="
				+ credit_number + ", jd_acct_id=" + jd_acct_id + ", act_status=" + act_status + "]";
	}

	public void clear() {
		user_credit_id = "";
		user_id = "";
		eff_date = null;
		exp_date = null;
		eff_flag = "";
		local_net = "";
		credit_type = "";
		credit_number = 0;
		jd_acct_id = "";
		act_status = "";
	}
}
